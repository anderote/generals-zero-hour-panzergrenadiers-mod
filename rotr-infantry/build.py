def token_map():
    m = dict(portdefs.RENAMES)
    m.update(PREREQ_REMAP)
    m.update(FX_REMAP)
    m.update(AUDIO_REMAP)
    return m


def side_fix(name, text):
    if name in ("RussianInfantryShmelTrooper", "RussiaInfantryShockTrooper",
                "RussiaInfantryShockTrooper_Var1"):
        text, n = re.subn(r"^(\s*Side\s*=\s*)Russia\b", r"\1ChinaTankGeneral",
                          text, flags=re.M)
        assert n >= 1, "no Side line found in %s" % name
    return text


