def donor_strings():
    fp = os.path.join(HERE, "cache", "fetched", "Rotr_English", "Data", "Generals.str")
    if not os.path.exists(fp):
        rotrfetch.RotrArt().fetch("Data\\Generals.str")
    raw = open(fp, "rb").read().decode("latin-1")
    pat = re.compile(r"^([A-Za-z0-9_]+:[A-Za-z0-9_\-]+)[ \t]*\r?\n(.*?)\r?\nEND",
                     re.S | re.M)
    return {m.group(1).lower(): m.group(2).strip() for m in pat.finditer(raw)}


def build_str_fragment():
    donor = donor_strings()
    out = []
    for ours, theirs in STR_FROM_DONOR:
        text = donor[theirs.lower()]
        # donor text fixes (documented in README):
        # - drop the berserker flavor line (mechanic not ported)
        text = text.replace('\\n Fights harder when a nearby comrade falls.', "")
        # - donor typo: doubled closing quote on the Shock Trooper tooltip
        text = re.sub(r'""\s*$', '"', text)
        assert text.startswith('"') and text.endswith('"') and text.count("\n") == 0, \
            "unexpected donor string shape for %s: %r" % (theirs, text)
        out.append("%s\n%s\nEND\n" % (ours, text))
    return "\n".join(out)


def ability_buttons(ddefs):
    out = []
    for name in ("Command_RussiaShmellTrooperSmokeRocket",
                 "Command_RussiaShmellTrooperAntiToxinRocket",
                 "Command_RussiaShockTrooperSwitchToRockets",
                 "Command_RussiaShockTrooperSwitchToTeslaGun"):
        _rel, text = ddefs[("CommandButton", name)]
        # drop the ROTR science gate on the tesla switch (Kwai can't buy
        # SCIENCE_TeslaTech); also the donor's empty "Science =" line
        text, _ = portlib.strip_lines(text, r"^\s*Science\s*=")
        text = retoken(text, token_map())
        text = retoken(text, LABEL_RENAMES)
        out.append(text.rstrip() + "\n")
    return "\n".join(out)


# ------------------------------------------------------------------- main
def main():
    print("== extracting effective INI space (fresh) ==")
    subprocess.run([sys.executable, os.path.join(WORK, "extract_effective.py")],
                   check=True, stdout=subprocess.DEVNULL)

    print("== parsing trees ==")
    ddefs, dbyname = load_tree(portdefs.DONOR_INI)
    tmap = token_map()

    # ---------------- object files
    entries = {}   # internal path -> bytes
    shipped_ini = {}   # internal path -> final text (for verification)
    for path, blocks in OBJECT_FILES.items():
        parts = [HEADER]
        for t, name in blocks:
            _rel, text = ddefs[(t, name)]
            text = edit_block(t, name, text)
            text = side_fix(name, text)
            text = retoken(text, tmap)
            parts.append(text.rstrip() + "\n\n")
        final = "".join(parts)
        shipped_ini[path] = final
        entries[path] = final.encode("latin-1")

    # ---------------- shared-file fragments + baked copies
    os.makedirs(FRAG_DIR, exist_ok=True)
    frag_texts = {}
    for fname, names in SHARED_APPENDS.items():
        parts = []
        for name in names:
            key = None
            for t in ("Weapon", "Armor", "Locomotor", "FXList", "ParticleSystem",
                      "ObjectCreationList"):
                if (t, name) in ddefs:
                    key = (t, name)
                    break
            assert key, "donor block not found: %s" % name
            _rel, text = ddefs[key]
            text = edit_block(key[0], name, text)
            text = retoken(text, tmap)
            parts.append(text.rstrip() + "\n\n")
        frag_texts[fname] = "".join(parts)

    frag_texts["CommandSet.ini"] = UNIT_COMMANDSETS
    frag_texts["CommandButton.ini"] = ability_buttons(ddefs) + "\n" + CONSTRUCT_BUTTONS
    frag_texts["Generals.str"] = build_str_fragment()

    for fname, text in frag_texts.items():
        with open(os.path.join(FRAG_DIR, fname + ".frag"), "w", newline="\n") as f:
            f.write(text)
    print("wrote %d fragments" % len(frag_texts))

    # bake shared INI files (effective copy + appends); command wiring is NOT baked
    for fname in SHARED_APPENDS:
        eff_fp = os.path.join(WORK, "effective", "Data", "INI", fname)
        base_bytes = open(eff_fp, "rb").read()
        baked = base_bytes + APPEND_BANNER.encode("latin-1") + \
            frag_texts[fname].encode("latin-1")
        path = "Data\\INI\\" + fname
        entries[path] = baked
        shipped_ini[path] = baked.decode("latin-1")

    # ---------------- mapped images (force-ship pages under Rotr_ names)
    donor_mi, _ = load_tree(os.path.join(portdefs.DONOR_INI, "MappedImages"))
    art = rotrfetch.RotrArt()
    page_rename = {}
    mi_parts = [HEADER]
    for name in MAPPED_IMAGES:
        _rel, text = donor_mi[("MappedImage", name)]
        m = re.search(r"^\s*Texture\s*=\s*([A-Za-z0-9_\-\.]+)", text, re.M)
        page = m.group(1)                     # e.g. SNRUserInterface512_002.tga
        if page not in page_rename:
            page_rename[page] = "Rotr_" + page
        text = text.replace(page, page_rename[page])
        mi_parts.append(text.rstrip() + "\n\n")
    mi_path = "Data\\INI\\MappedImages\\HandCreated\\RotrInfantryMappedImages.INI"
    mi_final = "".join(mi_parts)
    entries[mi_path] = mi_final.encode("latin-1")
    shipped_ini[mi_path] = mi_final

    for page, new in sorted(page_rename.items()):
        _ipath, local = art.fetch(page)
        entries["Art\\Textures\\" + new] = open(local, "rb").read()
    print("mapped-image pages shipped (renamed): %s" % sorted(page_rename.values()))

    # ---------------- W3D + texture closure
    art_needed = collect_art_tokens(shipped_ini)
    json.dump(art_needed, open(os.path.join(WORK, "art_needed.json"), "w"), indent=1)
    manifest = resolve_art_assets(art_needed)
    for ipath, local in manifest["ship"]:
        entries[ipath] = open(local, "rb").read()

    # ---------------- write archive
    big_entries = [BigEntry(p, entries[p]) for p in sorted(entries, key=str.lower)]
    out_fp = os.path.join(HERE, ARCHIVE)
    write_big_file(big_entries, out_fp)
    print("wrote %s: %d files, %.1f MB" % (
        ARCHIVE, len(big_entries), os.path.getsize(out_fp) / 1e6))

    # extracted/ mirror for eyeballing
    for p, data in entries.items():
        fp = os.path.join(EXTRACT_DIR, p.replace("\\", "/"))
        os.makedirs(os.path.dirname(fp), exist_ok=True)
        open(fp, "wb").write(data)

    # build manifest for README/integrate bookkeeping
    owners = json.load(open(os.path.join(WORK, "owners.json")))
    json.dump({
        "baked_shared_sources": {("Data\\INI\\" + f): owners.get("Data\\INI\\" + f)
                                 for f in SHARED_APPENDS},
        "art_shipped": sorted(p for p, _ in manifest["ship"]) +
                       sorted("Art\\Textures\\" + v for v in page_rename.values()),
        "art_reused_from_base": sorted("%s (%s)" % (t, o) for t, o in manifest["base"]),
        "page_renames": page_rename,
    }, open(os.path.join(WORK, "build_manifest.json"), "w"), indent=1)

    # ---------------- verification
    print("== verification ==")
    verify_lib.verify_all(out_fp, frag_texts, dbyname)
    print("BUILD OK")


# ------------------------------------------------------------- art helpers
ART_LINE = re.compile(
    r"^\s*(Model|ModelNames|Animation|IdleAnimation|AnimationSet)\s*=\s*(.+)$", re.M)
TEX_LINE = re.compile(
    r"^\s*(Texture|ParticleName|ShadowTexture)\s*=\s*([A-Za-z0-9_\-\.; ]+)$", re.M)


def collect_art_tokens(shipped_ini):
    models, textures = set(), set()
    for path, text in shipped_ini.items():
        if not path.lower().endswith(".ini"):
            continue
        # only OUR appended/object content: for baked shared files, scan only
        # the appended tail (base content is the effective space's business)
        if path.startswith("Data\\INI\\") and "\\" not in path[len("Data\\INI\\"):]:
            text = text.split("appended by rotr-infantry", 1)[-1]
        if "MappedImages" in path:
            continue   # pages handled separately
        for m in ART_LINE.finditer(text):
            val = portlib.strip_comments_line(m.group(2))
            for tok in re.findall(r"[A-Za-z0-9_\-\.]+", val):
                if tok.upper() in ("NONE",) or re.match(r"^[\d\.\-]+$", tok):
                    continue
                for part in tok.split("."):
                    if part and not re.match(r"^[\d\-]+$", part):
                        models.add(part)
        for m in TEX_LINE.finditer(text):
            val = portlib.strip_comments_line(m.group(2)).strip().rstrip(";").strip()
            for tok in re.findall(r"[A-Za-z0-9_\-\.]+", val):
                if tok.upper() == "NONE":
                    continue
                textures.add(tok.rsplit(".", 1)[0])
    return {"models": sorted(models), "textures": sorted(textures)}


def resolve_art_assets(art_needed):
    base = resolve_art.base_art_index()
    rotr = rotrfetch.RotrArt()
    manifest = {"ship": [], "base": [], "missing": []}
    fetched_w3d = []
    for tok in art_needed["models"]:
        f = tok.lower() + ".w3d"
        if f in base:
            manifest["base"].append((tok, base[f]))
            continue
        hit = rotr.find(tok + ".w3d")
        if not hit:
            manifest["missing"].append(("w3d", tok))
            continue
        path, local = rotr.fetch(hit[1])
        manifest["ship"].append((path, local))
        fetched_w3d.append(local)

    tex_all = set(t.lower() for t in art_needed["textures"])
    for local in fetched_w3d:
        for t in resolve_art.w3d_texture_names(local):
            tex_all.add(t.rsplit(".", 1)[0].lower())
    for tok in sorted(tex_all):
        cands = [tok + ".dds", tok + ".tga"]
        in_base = [c for c in cands if c in base]
        if in_base:
            manifest["base"].append((tok, base[in_base[0]]))
            continue
        hit = None
        for c in cands:
            hit = rotr.find(c)
            if hit:
                break
        if not hit:
            manifest["missing"].append(("texture", tok))
            continue
        path, local = rotr.fetch(hit[1])
        manifest["ship"].append((path, local))

    if manifest["missing"]:
        raise SystemExit("MISSING ART: %r" % manifest["missing"])
    return manifest


if __name__ == "__main__":
    main()
